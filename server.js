// Helper function to get knowledge entries matching services
const getKnowledgeForServices = async (services) => {
    try {
        const result = await pool.query("SELECT * FROM knowledgebase");
        const rows = result.rows;

        if (!rows || rows.length === 0) {
            return [];
        }

        // Match entries based on service names or tags
        const matchedEntries = rows.filter(entry => {
            const entryTitle = entry.title?.toLowerCase() || '';
            const entryTags = (() => {
                try {
                    return JSON.parse(entry.tags || '[]').map(t => t.toLowerCase());
                } catch {
                    return [];
                }
            })();

            // Check if any service matches this entry
            return services?.some(service => {
                const serviceLower = service.toLowerCase();
                return entryTitle.includes(serviceLower) ||
                    serviceLower.includes(entryTitle) ||
                    entryTags.some(tag => serviceLower.includes(tag) || tag.includes(serviceLower));
            });
        });

        console.log(`📚 Found ${matchedEntries.length} knowledge entries for services:`, services);
        return matchedEntries;
    } catch (err) {
        console.error("Error fetching knowledgebase:", err);
        return [];
    }
};

