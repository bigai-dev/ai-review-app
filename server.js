// Update submission (Status, Phone, etc)
app.patch('/api/submissions/:id', async (req, res) => {
    const { status, phone, feedback } = req.body;
    const { id } = req.params;

    // Dynamic query construction
    const updates = [];
    const values = [];
    let idx = 1;

    if (status) { updates.push(`status = $${idx++}`); values.push(status); }
    if (phone) { updates.push(`phone = $${idx++}`); values.push(phone); }
    if (feedback) { updates.push(`feedback = $${idx++}`); values.push(feedback); }

    if (updates.length === 0) return res.json({ success: true, message: "No updates" });

    values.push(id);

    const sql = `UPDATE submissions SET ${updates.join(", ")} WHERE id = $${idx}`;

    try {
        const result = await pool.query(sql, values);
        invalidateCache('stats');
        res.json({ success: true, changes: result.rowCount });
    } catch (err) {
        res.status(500).json({ error: err.message });
    }
});

